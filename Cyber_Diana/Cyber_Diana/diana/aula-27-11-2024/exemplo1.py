#Ordenar lista
minha_lista = [1,3,2,4,6,7,0,8,9,5]

print('---Lista original---')
print(minha_lista)
minha_lista.sort() #Ordem crescente 

print('---Lista ordenada crescente---')
print(minha_lista)

print('---Lista ordenada decrescente---')
minha_lista.sort(reverse = True) #Ordem decrescente
print(minha_lista)
